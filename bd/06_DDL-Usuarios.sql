CREATE TABLE IF NOT EXISTS USUARIOS (
  IDUSUARIO INTEGER PRIMARY KEY,
  ACTIVO TEXT NOT NULL CHECK (length(ACTIVO) = 1),
  CLAVE TEXT NOT NULL,
  CONFIRMADO TEXT NOT NULL CHECK (length(CONFIRMADO) = 1),
  DOCUMENTO INTEGER NOT NULL UNIQUE,
  FECHA_NACIMIENTO TEXT NOT NULL,
  MAIL_INSTITUCIONAL TEXT,
  MAIL_PERSONAL TEXT,
  PRIMER_APELLIDO TEXT NOT NULL,
  PRIMER_NOMBRE TEXT NOT NULL,
  SEGUNDO_APELLIDO TEXT,
  SEGUNDO_NOMBRE TEXT,
  TELEFONO TEXT,
  TIPO_USUARIO TEXT NOT NULL,
  USUARIO TEXT NOT NULL UNIQUE,
  IDDEPARTAMENTO INTEGER NOT NULL,
  IDGENERO INTEGER NOT NULL,
  IDITR INTEGER NOT NULL,
  IDLOCALIDAD INTEGER NOT NULL,
  FOREIGN KEY (IDDEPARTAMENTO) REFERENCES DEPARTAMENTOS(IDDEPARTAMENTO),
  FOREIGN KEY (IDGENERO) REFERENCES GENEROS(IDGENERO),
  FOREIGN KEY (IDITR) REFERENCES ITRS(IDITR),
  FOREIGN KEY (IDLOCALIDAD) REFERENCES LOCALIDADES(IDLOCALIDAD)
);
